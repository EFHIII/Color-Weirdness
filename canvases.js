function visible(el) {
  let bounds = el.getBoundingClientRect();
  return bounds.top <= window.innerHeight && bounds.bottom >= 0;
}

function enlargePixels(fromImage, toImage, w, h, scale) {
  for(let y = 0; y < h; y++) {
    for(let x = 0; x < w; x++) {
      let r = fromImage.data[(x + y * w) * 4 + 0];
      let g = fromImage.data[(x + y * w) * 4 + 1];
      let b = fromImage.data[(x + y * w) * 4 + 2];
      for(let Y = 0; Y < 3 * scale; Y++) {
        for(let X = 0; X < scale; X++) {
          toImage.data[(X + x * scale * 3 + (Y + y * scale * 3) * w * scale * 3) * 4 + 0] = r;
        }
        for(let X = scale; X < 2 * scale; X++) {
          toImage.data[(X + x * scale * 3 + (Y + y * scale * 3) * w * scale * 3) * 4 + 1] = g;
        }
        for(let X = 2 * scale; X < 3 * scale; X++) {
          toImage.data[(X + x * scale * 3 + (Y + y * scale * 3) * w * scale * 3) * 4 + 2] = b;
        }
      }
    }
  }
}

function superCanvas(c1, c2, w, h) {
  let img = c1.getImageData(0, 0, w, h);

  c2.fillStyle = '#000';
  c2.fillRect(0, 0, w * 6, h * 6);

  let ans = c2.getImageData(0, 0, w * 6, h * 6);

  enlargePixels(img, ans, w, h, 2);

  c2.putImageData(ans, 0, 0);
}

